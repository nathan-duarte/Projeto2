class Scratch10 {

    public static void main(String[] args) {

        var codigo = 0;

        if (codigo >= 1) {

            System.out.println("Um");
        }

        if (codigo >= 2) {

            System.out.println("Dois");
        }

        if (codigo >= 3) {

            System.out.println("Três");
        }

        else {

            if (codigo <= 0) {

                System.out.println("Código inválido");
            }
        }
        
    }
}

